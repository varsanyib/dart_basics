class Szamol {
  static int osszead(int a, int b) {
    return a + b;
  }

  static int szoroz(int a, int b) {
    return a * b;
  }

  static int differencia(int a, int b) {
    return a - b;
  }

  static double oszt(int a, int b) {
    return a / b;
  }

  static int moduloOszt(int a, int b) {
    return a % b;
  }
}
